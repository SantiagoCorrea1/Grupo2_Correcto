    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        if (boxNewAmount.getText().equals("") || boxNewCode.getText().equals("") || boxNewPrice.getText().equals("")) {
            showMessageDialog(null, "debe llenar todos los campos");
        } else {
            Pass newPass = new Pass(Double.parseDouble(boxNewPrice.getText()), boxNewCode.getText(), Integer.parseInt(boxNewAmount.getText()));
            if (mainClass.getDptSales().passExits(boxNewCode.getText())) {
                showMessageDialog(null, "el abono ya existe");
            } else {
                mainClass.getDptSales().newPass(newPass);
                showMessageDialog(null, "el abono fue agregado correctamente");
            }
        }
        setNull();

    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        // TODO add your handling code here:
        if (boxEditAmount.getText().equals("") || boxEditCode.getText().equals("") || boxEditPrice.getText().equals("")) {
            showMessageDialog(null, "debe llenar todos los campos");
        } else {
            Pass editPass = new Pass(Double.parseDouble(boxEditPrice.getText()), boxEditCode.getText(), Integer.parseInt(boxEditAmount.getText()));
            mainClass.getDptSales().editPass(boxEditCode.getText(), editPass);
        }
        setNull();
    }//GEN-LAST:event_buttonEditActionPerformed

