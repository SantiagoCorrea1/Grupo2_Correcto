     private void loadTicketsToTable() {
     // Obtener el modelo de datos de la tabla jTable1
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    // Limpiar la tabla antes de cargar los datos
    model.setRowCount(0);

    // Recorrer la lista de planes y agregarlos a la tabla
    //public Ticket(double price, String code, int age_restriction) 
    for (Ticket ticket : mainClass.getDptSales().arraysControl.getTickets()) {
        Object[] newRow = {ticket.getCode(), ticket.getPrice(), ticket.getAge_restriction()};
        model.addRow(newRow);
    }
    
}
